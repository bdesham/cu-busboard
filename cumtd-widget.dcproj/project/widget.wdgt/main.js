function get_current_time()
{
	var now = new Date();
	var hour = now.getHours();
	var minutes = now.getMinutes();
	
	if (minutes < 10)
		minutes = "0" + minutes;
	
	if (hour <= 11)
		return hour + ":" + minutes + " AM";
	else if (hour == 12)
		return "12:" + minutes + " PM";
	else
		return (hour - 12) + ":" + minutes + " PM";

}

